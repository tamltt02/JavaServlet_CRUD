    private void showData(List<QLTaiKhoan> listQLTK) {
        dtm.setRowCount(0);
        for (QLTaiKhoan qLTaiKhoan : tkService.getAll()) {
            dtm.addRow(new Object[]{qLTaiKhoan.getIdTaiKhoan(), qLTaiKhoan.getTenTaiKhoan(), qLTaiKhoan.getMatKhau()});
        }
    }

