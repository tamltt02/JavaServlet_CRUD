    private void tblDangKyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tblDangKyActionPerformed
        // TODO add your handling code here:
        String tk = txtTenTaiKhoan.getText().trim();
        String mk = String.valueOf(pwtMatKhau.getPassword()).trim();
        String nhapLaiMK = String.valueOf(pwtNhapLaiMK.getPassword()).trim();
        if (mk.equals(nhapLaiMK)) {
            QLTaiKhoan qlTK = new QLTaiKhoan(null, tk, nhapLaiMK);
            JOptionPane.showMessageDialog(this, tkService.add(qlTK));
            showData(listQLTK = tkService.getAll());
        } else {
            JOptionPane.showMessageDialog(this, "Mật Khẩu Không Khớp, Vui Lòng Nhập Lại!!!");
        }
    }//GEN-LAST:event_tblDangKyActionPerformed

