    private String genMa() {
        String maMS = " ";
        String s1 = "MS";
        for (int i = 1; i < listMauSac.size() + 2; i++) {
            maMS = s1 + i;
        }
        return maMS;
    }

    private void clearForm() {
        txtTenMauSac.setText("");
        lbMa.setText("--");
    }

