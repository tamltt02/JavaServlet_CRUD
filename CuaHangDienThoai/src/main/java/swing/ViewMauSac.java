    private void btnThemMauSacActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemMauSacActionPerformed
        QLMauSac mauSac = new QLMauSac();
        if (txtTenMauSac.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Không được để trống tên");
            return;
        }
        mauSac.setMa(genMa());
        mauSac.setTen(txtTenMauSac.getText());
        mauSac.setTrangThai(0);

        JOptionPane.showMessageDialog(this, iMauSacService.save(mauSac));
        loadTable();
        clearForm();
    }//GEN-LAST:event_btnThemMauSacActionPerformed

    private void btnDoiTrangThaiMauSacActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoiTrangThaiMauSacActionPerformed
        int row = tblMauSac.getSelectedRow();
        if (row <= -1) {
            JOptionPane.showMessageDialog(this, "Xin mời chọn để đổi trạng thái");
            return;
        }
        QLMauSac mauSac = listMauSac.get(row);
        if (mauSac.getTrangThai() == 0) {
            mauSac.setTrangThai(1);
        } else {
            mauSac.setTrangThai(0);
        }

        int chon = JOptionPane.showConfirmDialog(this, "Bạn có muốn đổi trạng thái", "Đổi trạng thái", JOptionPane.YES_NO_OPTION);
        if (chon != JOptionPane.YES_OPTION) {
            return;
        }
        JOptionPane.showMessageDialog(this, iMauSacService.update(mauSac));
        loadTable();
        clearForm();

    }//GEN-LAST:event_btnDoiTrangThaiMauSacActionPerformed

    private void tblMauSacMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMauSacMouseClicked
        int row = tblMauSac.getSelectedRow();
        if (row <= -1) {
            return;
        }

        String ma = tblMauSac.getValueAt(row, 0).toString();
        String ten = tblMauSac.getValueAt(row, 1).toString();

        lbMa.setText(ma);
        txtTenMauSac.setText(ten);
    }//GEN-LAST:event_tblMauSacMouseClicked

